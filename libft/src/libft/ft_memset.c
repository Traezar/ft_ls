#include "../../includes/libft.h"

void		*ft_memset(void *b, int c, size_t len)
{
	size_t		n;
	char		*x;

	n = 0;
	x = b;
	while (n < len)
	{
		*(x + n) = c;
		n++;
	}
	return (b);
}
